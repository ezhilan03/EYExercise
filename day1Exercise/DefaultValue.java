package InternExercise.day1Exercise;

public class DefaultValue {
	static int i;
	static float f;
	static char c;
	static short s;
	static long l;
	static double d;
	static boolean b;
	static byte by;
	public static void main(String[] args) {
		System.out.println("The default value of integer is... "+i);
		System.out.println("The default value of float is... "+f);
		System.out.println("The default value of character is... "+c);
		System.out.println("The default value of short is... "+s);
		System.out.println("The default value of long is... "+l);
		System.out.println("The default value of double is... "+d);
		System.out.println("The default value of boolean is... "+b);
		System.out.println("The default value of byte is... "+by);
	}

}
